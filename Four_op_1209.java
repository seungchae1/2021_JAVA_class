public class Four_op_1209 {
	public static void main(String args[]){
		double a = 3.5;
		double b = 7.2;
		System.out.print(a+"+"+b+"= ");
		System.out.println(a+b);
		System.out.print(a+"-"+b+"= ");
		System.out.println(a-b);
		System.out.print(a+"*"+b+"= ");
		System.out.println(a*b);
		System.out.print(a+"/"+b+"= ");
		System.out.println(a/b);
	}
}